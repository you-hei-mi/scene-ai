export interface FileFormatIconProps {
  /** Normalized key (e.g. `pdf`, `docx`, `xlsx`). */
  format?: string;
  className?: string;
}

/**
 * Render an SVG icon based on a normalized format key.
 *
 * @param props.format - Normalized key derived from MIME type (see `getFileFormatKey`).
 */
export const FileFormatIcon: React.FC<FileFormatIconProps> = ({ format, className, ...props }) => {
  if (!format) {
    return null;
  }

  const icon = fileFormatIconsMap[format.toLowerCase()];
  if (!icon) {
    return null;
  }

  const iconElement = icon as React.ReactElement<{ className?: string } & React.SVGAttributes<SVGSVGElement>>;

  return React.cloneElement(iconElement, {
    className: `${iconElement.props?.className || ""} ${className || ""}`.trim(),
    ...props,
  });
};

